package Egzersizler;

public class C13_Egzersizler {
    public static void main(String[] args) {

        int num=11;
        num=num-2;
        num+=3;
        num++;
        System.out.println(num);

        int num1=11;
        num1=num1+2;
        num1--;
        num1--;
        System.out.println(num1);

        int num2=11;
        num2+=3;
        num2+=2;
        num2--;
        System.out.println(num2);




    }
}
